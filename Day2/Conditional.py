# conditional stateements: They are used to perform different actions based on different conditions.

# if statement: It is used to execute a block of code if a specified condition is true.

a=10
b=20

if a < b:
    print("a is less than b")

# if-else statement: It is used to execute a block of code if a specified condition is true, and another block of code if the condition is false.
a=10
b=20

if a < b:
    print("a is less than b")
else:
    print("a is not less than b")

# elif statement: It is used to specify a new condition to test, if the first condition is false.

a=19
b=30

if a>b:
    print("a is greater than b")

elif a<b:
    print("a is less than b")

else:
    print("a is equal to b")

    